/**
 * Represents the two colors of playing cards in the Jest card game.
 * This enumeration defines the binary color classification used for suit cards,
 * following traditional playing card color conventions.
 *
 * <p>Color associations with suits:</p>
 * <ul>
 *   <li>RED: Hearts (♥) and Diamonds (♦)</li>
 *   <li>BLACK: Spades (♠) and Clubs (♣)</li>
 * </ul>
 *
 * @author Jest Game & Gatien Genevois & Sacha Himber
 * @version 1.0
 * @see SuitCard
 * @see Sign
 */
public enum Color {
    /** The red color, associated with Hearts and Diamonds */
    RED,
    /** The black color, associated with Spades and Clubs */
    BLACK
}
