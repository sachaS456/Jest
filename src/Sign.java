/**
 * Represents the four suits/signs of playing cards.
 * This enumeration defines the traditional card suits used in the Jest game.
 *
 * @author Jest Game & Gatien Genevois & Sacha Himber
 * @version 1.0
 */
public enum Sign {
    /** The spade suit (♠) */
    SPADE,
    /** The club suit (♣) */
    CLUB,
    /** The diamond suit (♦) */
    DIAMOND,
    /** The heart suit (♥) */
    HEARTH
}
